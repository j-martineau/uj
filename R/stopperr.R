  if (!OkVals) {Errors <- base::c(Errors, "[VALS] must be complete and atomic (?cmp_atm).")}
  if (!OkNamed) {Errors <- base::c(Errors, "all [...] args must be named.")}
  if (!OkBlank) {Errors <- base::c(Errors, "[...] arg names may not be blank strings (\"\").")}
  if (!OkUnq) {Errors <- base::c(Errors, "[...] arg names must be unique.")}
  if (!base::is.null(Errors)) {uj::stopperr(Errors, PKG = "uj")}
  X <- base::list(...)
  Atm <- base::sapply(X, uj:::.POP) & base::sapply(X, uj:::.ATM)
  if (A & !base::all(Atm)) {uj::stopperr("When [A = TRUE], all [...] args must be populated and atomic (?pop_atm).", PKG = "uj")}
  if (!A) {
    Vls <- base::sapply(X, atm_vls)
    Dtf <- base::sapply(X, atm_dtf)
    if (!base::all(Atm | Vls | Dtf)) {uj::stopperr("All [...] args must be populated and atomic (?pop_atm), atomic vlists (?atm_vls), or atomic data.frames (?atm_dtf).", PKG = "uj")}
  }
  for (i in 1:nDots) {if (!valid(X[[i]])) {uj::bankerr("[", Labels[i], "] contains 1 or more values not in [VALS].", GENS = 1, D = "")}}
}

#' @rdname stopperr
#' @export
check_chars <- function(CHARS, ..., A = TRUE) {
  valid <- function(y) {
    if (base::is.character(y)) {
      y <- base::paste0(uj::av(y), collapse = "")
      y <- uj::av(base::strsplit(y, "", fixed = T))
      base::all(y %in% CHARS)
    } else if (base::is.data.frame(y)) {base::all(base::apply(y, 2, valid))}
    else if (base::is.list(y)) {base::all(base::sapply(y, valid))}
    else {F}
  }
  nDots <- base::...length()
  Labels <- base::...names()
  OkN <- nDots > 0
  OkA <- uj:::.cmp_lgl_scl(A)
  OkNamed <- base::length(Labels) == nDots
  OkBlank <- !base::any(Labels == "")
  OkUnq <- base::length(Labels) == base::length(base::unique(Labels))
  OKChars <- uj:::.cmp_str(CHARS)
  Errors <- NULL
  if (!OkN) {Errors <- base::c(Errors, "[...] is empty.")}
  if (!OkA) {Errors <- base::c(Errors, "[A] must be TRUE or FALSE.")}
  if (!OKChars) {Errors <- base::c(Errors, "[CHARS] must be complete and of mode string (?cmp_str).")}
  if (!OkNamed) {Errors <- base::c(Errors, "all [...] args must be named.")}
  if (!OkBlank) {Errors <- base::c(Errors, "[...] arg names may not be blank strings (\"\").")}
  if (!OkUnq) {Errors <- base::c(Errors, "[...] arg names must be unique.")}
  if (!base::is.null(Errors)) {uj::stopperr(Errors, PKG = "uj")}
  X <- base::list(...)
  Chr <- base::sapply(X, uj:::.POP) & base::sapply(X, uj:::.CHR)
  if (A | !base::all(Chr)) {uj::stopperr("When [A = TRUE], all [...] args must be populated and of mode character (?pop_chr).", PKG = "uj")}
  if (!A) {
    Vls <- base::sapply(X, chr_vls)
    Dtf <- base::sapply(X, chr_dtf)
    if (!base::all(Chr | Vls | Dtf)) {uj::stopperr("All [...] args must be populated and of mode character (?pop_chr), character vlists (?chr_vls), or character data.frames (?chr_dtf).", PKG = "uj")}
  }
  for (i in 1:nDots) {if (!valid(X[[i]])) {uj::bankerr("[", Labels[i], "] contains 1 or more characters not in [CHARS].", GENS = 1, D = "")}}
}

#' @rdname stopperr
#' @export
check_dots <- function(SPEC, ..., NAMED = FALSE) {
  nDots <- base::...length()
  Labels <- base::...names()
  OkNamed <- uj:::.cmp_lgl_scl(NAMED)
  OkSpec <- uj:::.cmp_chr_vec(SPEC)
  if (OkSpec) {
    Combos <- uj::av(base::strsplit(SPEC, "|", fixed = TRUE))
    for (Combo in Combos) {
      Props <- uj::av(base::strsplit(Combos, "_", fixed = TRUE))
      if (!base::all(base::tolower(Props) %in% uj:::.ppp)) {OkSpec <- FALSE}
      else if (base::length(Props) != base::length(base::unique(Props))) {OkSpec <- FALSE}
    }}
  OkN <- nDots > 0
  OkNamed <- !base::isTRUE(NAMED) | base::length(Labels) == nDots
  OkBlank <- !base::any(Labels == "")
  OkUnq <- base::length(Labels) == base::length(base::unique(Labels))
  Errors <- NULL
  if (!OkNamed) {Errors <- base::c(Errors, "[NAMED] must be TRUE or FALSE.")}
  if (!OkN) {Errors <- base::c(Errors, "[...] is empty.")}
  if (!OkSpec) {Errors <- base::c(Errors, "[SPEC] must be complete and character (?cmp_chr).")}
  if (!OkNamed) {Errors <- base::c(Errors, "all [...] args must be named with [NAMED = TRUE].")}
  if (!OkBlank) {Errors <- base::c(Errors, "[...] arg names may not be blank strings (\"\").")}
  if (!OkUnq) {Errors <- base::c(Errors, "[...] arg names must be unique.")}
  if (!base::is.null(Errors)) {uj::stopperr(Errors, PKG = "uj")}
  if (!base::all(base::sapply(base::list(...), uj::PPP, spec = SPEC))) {uj::stopperr(base::paste0("All [...] args must be ", uj::spec_concise(SPEC), "."))}
}

#' @rdname stopperr
#' @export
check_when <- function(WHENS, VALS, ...) {
  Labels <- base::...names()
  nDots <- base::...length()
  OkN <- nDots == 2
  OkNamed <- base::length(Labels) == nDots
  OkBlank <- !base::any(Labels == "")
  OkUnq <- base::length(Labels) == base::length(base::unique(Labels))
  OkWhens <- uj:::.pop_atm(WHENS)
  OkVals <- uj:::.pop_atm(VALS)
  OkNeq <- base::length(WHENS) == length(VALS)
  OkScl <- uj:::.atm_scl(..1) & uj:::.atm_scl(..2)
  OkWhens2 <- base::ifelse(OkWhens & OkVals, uj:::.compat(WHENS, ..1), T)
  OkVals2 <- base::ifelse(OkWhens & OkVals, uj:::.compat(VALS, ..2), T)
  Errors <- NULL
  if (!OkN) {Errors <- base::c(Errors, "There must be two [...] args")}
  if (!OkNamed) {Errors <- base::c(Errors, "all [...] args must be named.")}
  if (!OkBlank) {Errors <- base::c(Errors, "[...] arg names may not be blank.")}
  if (!OkUnq) {Errors <- base::c(Errors, "[...] arg names must be unique.")}
  if (!OkVals) {Errors <- base::c(Errors, "[VALS] must be non-empty and atomic.")}
  if (!OkWhens) {Errors <- base::c(Errors, "[WHENS] must be non-empty and atomic.")}
  if (!OkNeq) {Errors <- base::c(Errors, "[WHENS] and [VALS] must be of the same length.")}
  if (!OkScl) {Errors <- base::c(Errors, "Both args in [...] must be atomic and scalar (?atm_scl).")}
  if (!OkWhens2) {Errors <- base::c(Errors, "[VALS] and [..2] are of incompatible (?compatible) modes.")}
  if (!OkVals2) {Errors <- base::c(Errors, "[WHENS] and [..1] are of incompatible (?compatible) modes.")}
  if (!base::is.null(Errors)) {uj::stopperr(Errors, PKG = "uj")}
  Labs1 <- base::paste0("[", Labels[1], "]")
  Labs2 <- base::paste0("[", Labels[2], "]")
  When <- ..1
  Val <- ..2
  i <- base::which(base::sapply(WHENS, uj::is_eq0, y = When))
  if (base::length(i) > 0) {
    Match <- VALS[i[1]]
    OkEq <- base::length(Val) == 1 & base::length(Match) == 1
    if (OkEq) {OkEq <- Val == Match}
    if (!OkEq) {
      if (base::is.character(Match)) {Match <- base::paste0("'", Match, "'")}
      uj::bankerr("When ", Labs1, " is ", When, ", ", Labs2, " must be ", Match, ".", GENS = 1, D = "")
    }}
}


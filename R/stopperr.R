  if (uj::is_err(STACK)) {STACK <- ""} else if (!base::is.character(STACK) | base::length(STACK) != 1) {STACK <- ""} else if (base::any(base::is.na(STACK))) {STACK <- ""}
  nDots <- base::...length()

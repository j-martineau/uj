#' @rdname values
#' @export
value_names <- function() {base::names(uj::values())}

#' @rdname values
#' @export
value_table <- function() {
  x <- uj::values()
  tibble::tibble(name = base::names(x), value = base::sapply(x, paste0, collapse = "|"), description = base::attr(x, "descriptions"))
}

#' @rdname values
#' @export
value <- function(...) {
  x <- base::as.character(base::match.call())
  x <- x[2:base::length(x)]
  uj::av(uj::values()[x])
}

